  `get_client_ip` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT NULL COMMENT '客户端IP (TCP/UDP_直连、TCP_SendProxy、HTTP_X-Forwarded-For)',
