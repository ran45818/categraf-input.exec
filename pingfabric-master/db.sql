  KEY `cmdb_vip_vip_IDX` (`vip`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='cmdb虚拟ip'
